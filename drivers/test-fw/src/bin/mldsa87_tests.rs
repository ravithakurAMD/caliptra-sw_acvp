fn test_mldsa_name() {
    let mut trng = unsafe {
        Trng::new(
            CsrngReg::new(),
            EntropySrcReg::new(),
            SocIfcTrngReg::new(),
            &SocIfcReg::new(),
            PersistentDataAccessor::new(),
        )
        .unwrap()
    };
    let mut entropy_gen = || {
        trng.generate4()
            .map_err(|e| caliptra_cfi_lib::CfiError(u32::from(e)))
    };

    // This needs to happen in the first test
    CfiCounter::reset(&mut entropy_gen);

    let abr_reg = unsafe { AbrReg::new() };
    let regs = abr_reg.regs();

    let name = regs.mldsa_name().read();

    // MLDSA_CORE_NAME from RTL: 64'h3837412D_44534D4C representing "MLDSA-87"
    assert_eq!(name, [0x44534D4C, 0x3837412D]);
}

fn test_gen_key_pair() {
    let mut trng = unsafe {
        Trng::new(
            CsrngReg::new(),
            EntropySrcReg::new(),
            SocIfcTrngReg::new(),
            &SocIfcReg::new(),
            PersistentDataAccessor::new(),
        )
        .unwrap()
    };
    let mut abr_reg = unsafe { AbrReg::new() };
    let mut ml_dsa87 = Mldsa87::new(&mut abr_reg);

    let mut hmac = unsafe { Hmac::new(HmacReg::new()) };
    let key_out_1 = KeyWriteArgs {
        id: KEY_ID,
        usage: KeyUsage::default().set_mldsa_key_gen_seed_en(),
    };

    hmac.hmac(
        HmacKey::from(&Array4x12::default()),
        HmacData::from(&[0]),
        &mut trng,
        HmacTag::Key(key_out_1),
        HmacMode::Hmac384,
    )
    .unwrap();

    let public_key = ml_dsa87
        .key_pair(Mldsa87Seed::Key(KeyReadArgs::new(KEY_ID)), &mut trng, None)
        .unwrap();
    assert_eq!(public_key, Mldsa87PubKey::from(PUBKEY));
}

fn test_sign() {
    let mut abr_reg = unsafe { AbrReg::new() };
    let mut ml_dsa87 = Mldsa87::new(&mut abr_reg);

    let mut trng = unsafe {
        Trng::new(
            CsrngReg::new(),
            EntropySrcReg::new(),
            SocIfcTrngReg::new(),
            &SocIfcReg::new(),
            PersistentDataAccessor::new(),
        )
        .unwrap()
    };

    let sign_rnd = Mldsa87SignRnd::default(); // Deterministic signing

    let signature = ml_dsa87
        .sign(
            Mldsa87Seed::Key(KeyReadArgs::new(KEY_ID)),
            &Mldsa87PubKey::from(PUBKEY), // Reuse SEED
            &MESSAGE.into(),
            &sign_rnd,
            &mut trng,
        )
        .unwrap();
    assert_eq!(signature, Mldsa87Signature::from(SIGNATURE));
}

